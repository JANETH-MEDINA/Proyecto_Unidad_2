public class Fecha {
    private int dia;
    private int mes;
    private int anualidad;

    public Fecha(int dia, int mes, int anualidad) {
        this.dia = dia;
        this.mes = mes;
        this.anualidad = anualidad;
    }

    public int getDia() {
        return dia;
    }

    public int getMes() {
        return mes;
    }

    public int getAnualidad() {
        return anualidad;
    }
}
